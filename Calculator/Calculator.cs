

namespace Calculator 
{
    public class Calculator
    {
        public int mathAddition(int x, int y)
        {
            return x +y;
        }

        public int mathSubtraction(int a, int b)
        {
            return a-b;
        }

        public int mathMultipy(int c, int d)
        {
            return c*d;
        }

        public int mathDivide(int m, int n)
        {
            return m/n;
        }
    }
}